package semuaperulangan;
public class HurufZA {
    public void ZA() {
        System.out.println("=================================================");
        System.out.println("                Huruf dari Z - A                 ");
        System.out.println("=================================================");
        for (char i = 'Z'; i >= 'A'; i--) {
            System.out.print(i + " ");
        }
        System.out.println("\n=================================================");
    }
}
